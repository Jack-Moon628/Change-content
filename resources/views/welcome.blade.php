    (function($) { // a self invoked anonymous function, todo: might be better to move it to site-scripts.js
    $('#header').bind('mega-menu.build', function (event,opts) {
        if(opts.headerStickyEnabled) // fix anchor position when sticky menu (we solve problem of scroll to anchor when header/menu is fixed/sticky)
        {
        $('.pganchor').each(function(){
            if($(this).offset().top-opts.headerStickyHeaderContainerHeight < opts.headerStickyStart)
            $(this).css({ top: '-'+$('#header').height()+'px' }); // case when menu is not shrinked yet (document.documentElement.scrollTop<opts.headerStickyStart)
            else
            $(this).css({ top: '-'+opts.headerStickyHeaderContainerHeight+'px' });
        });

        // solution is taken from here: https://stackoverflow.com/a/13067009/1147597
        (function(document, history, location) {
            var HISTORY_SUPPORT = !!(history && history.pushState);

            var anchorScrolls = {
            ANCHOR_REGEX: /^#[^ ]+$/,

            /**
             * Establish events, and fix initial scroll position if a hash is provided.
             */
            init: function() {
                this.scrollToCurrent();
                $(window).on('hashchange', $.proxy(this, 'scrollToCurrent'));
                $('body').on('click', 'a', $.proxy(this, 'delegateAnchors'));
            },

            /**
             * Return the offset amount to deduct from the normal scroll position.
             * Modify as appropriate to allow for dynamic calculations
             */
            getFixedOffset: function(target_el_top) {
                if(target_el_top-opts.headerStickyHeaderContainerHeight < opts.headerStickyStart)
                return $('#header').height(); // case when menu is not shrinked yet (document.documentElement.scrollTop<opts.headerStickyStart)
                else
                return opts.headerStickyHeaderContainerHeight;
            },

            /**
             * If the provided href is an anchor which resolves to an element on the
             * page, scroll to it.
             * @param  {String} href
             * @return {Boolean} - Was the href an anchor.
             */
            scrollIfAnchor: function(href, pushToHistory) {
                var match, anchorOffset;

                if(!this.ANCHOR_REGEX.test(href)) {
                return false;
                }

                match = document.getElementById(href.slice(1));

                if(match) {
                anchorOffset = $(match).offset().top - this.getFixedOffset($(match).offset().top);
                $('html, body').animate({ scrollTop: anchorOffset});

                // Add the state to history as-per normal anchor links
                if(HISTORY_SUPPORT && pushToHistory) {
                    history.pushState({}, document.title, location.pathname + href);
                }
                }

                return !!match;
            },

            /**
             * Attempt to scroll to the current location's hash.
             */
            scrollToCurrent: function(e) {
                if(this.scrollIfAnchor(window.location.hash) && e) {
                e.preventDefault();
                }
            },

            /**
             * If the click event's target was an anchor, fix the scroll position.
             */
            delegateAnchors: function(e) {
                var elem = e.target;
                if(this.scrollIfAnchor(elem.getAttribute('href'), true)) {
                e.preventDefault();
                }
            }
            };

            $(document).ready($.proxy(anchorScrolls, 'init'));
        })(window.document, window.history, window.location);
        }
    });
    }).apply(this, [jQuery]);
